export function searchEmojis(query: string): EmojiEntry[] {
  if (!query) return [];
  const lower = query.toLowerCase();
  const results: EmojiEntry[] = [];
  for (const [shortcode, emoji] of Object.entries(EMOJI_MAP)) {
    if (shortcode.includes(lower)) {
      results.push({ shortcode, emoji });
    }
  }
  // Sort: exact start matches first
  results.sort((a, b) => {
    const aStarts = a.shortcode.startsWith(lower) ? 0 : 1;
    const bStarts = b.shortcode.startsWith(lower) ? 0 : 1;
    return aStarts - bStarts || a.shortcode.localeCompare(b.shortcode);
  });
  return results.slice(0, 10);
}
